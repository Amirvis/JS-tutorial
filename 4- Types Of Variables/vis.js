// Number String Boolean Null Undefined

// We use "typeof" keyword to check the type of variables

// Number:

let age = 30;
console.log(typeof age);

// String:

let name = "John";
console.log(typeof name);

// Boolean(has only two values true or false):

let isStudent = false;
console.log(typeof isStudent);

let isAdult = true;
console.log(typeof isAdult);

// Null(null is a special value that represents the absence of a value it could be initialized later):

let country = null;
console.log(typeof country);

country = "Italy";
console.log(typeof country);

// Undefined(it could be initialized later):

let city;
console.log(typeof city);
city = "Rome";
console.log(typeof city);