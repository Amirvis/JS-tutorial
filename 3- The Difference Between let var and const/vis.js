// 3 ways to define a variable: var, let, const

// ----------------------------------------------------------------------

// let: You can change the value

let age = 23;
age = 24;

console.log(age);

// ----------------------------------------------------------------------

// const: You can't change the value

const birthYear = 2001;
// birthYear = 1990; Error

console.log(birthYear);

// ----------------------------------------------------------------------

// var: You can also change the value(we'll talk about this later)

var job = "programmer";
job = "teacher";

console.log(job);