// We have several types of naming conventions in variables

// ----------------------------------------------------------------------

// camel case:

let myFirstName;

let myLastName;

// ----------------------------------------------------------------------

// snake case: 

let my_first_name;

let my_last_name;

// ----------------------------------------------------------------------

// pascal case: 

let MyFirstName;

let MyLastName;

// ----------------------------------------------------------------------

// console.log() | use node to see the output

let country = "USA";
console.log(country);

console.log(30 + 20 + 15);

console.log("Hello World!");

// in JavaScript we'd better use camel case for naming variables

let firstName = "Alex";
console.log(firstName);

let ageAlex = 25;
console.log(ageAlex);

// for fixed variables we'd better use all the letters in uppercase

let PI = 3.14;
let GRAVITY = 9.8;

console.log(PI);
console.log(GRAVITY);